

$(document).ready(function(){
    $(window).scroll(function(){
        var scroll = $(window).scrollTop();
        if (scroll > 300) {
          $(".black").css("background" , "rgba(185, 202, 67, 0.78)");
        }
  
        else{
            $(".black").css("background" , "rgba(185, 202, 67, 0.78)");  	
        }
    })
  })

  